package bean;

public class ManagementBean {
	private int Mno;
	private String Mname;
	private String Msex;
	private String Mtel;
	private int Dbuil;
	public int getMno() {
		return Mno;
	}
	public void setMno(int mno) {
		Mno = mno;
	}
	public String getMname() {
		return Mname;
	}
	public void setMname(String mname) {
		Mname = mname;
	}
	public String getMsex() {
		return Msex;
	}
	public void setMsex(String msex) {
		Msex = msex;
	}
	public String getMtel() {
		return Mtel;
	}
	public void setMtel(String mtel) {
		Mtel = mtel;
	}
	public int getDbuil() {
		return Dbuil;
	}
	public void setDbuil(int dbuil) {
		Dbuil = dbuil;
	}
	
}
